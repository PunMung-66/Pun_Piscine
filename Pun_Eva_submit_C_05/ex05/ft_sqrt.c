#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	n;

	i = 1;
	n = 0;
	while (n <= nb && nb > 0 && i < 46341)
	{
		n = i * i;
		if (n == nb)
			return (i);
		i++;
	}
	return (0);
}
