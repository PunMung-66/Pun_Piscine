char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	if ('a' <= str[i] && str[i] <= 'z')
		str[i] = str[i] - 32;
	i++;
	while (str[i] != '\0')
	{
		if ('a' <= str[i] && str[i] <= 'z')
		{
			if ((str[i - 1] <= 47)
			|| (91 <= str[i - 1] && str[i - 1] <= 96)
			|| (123 <= str[i - 1] && str[i - 1] <= 126))
				str[i] = str[i] - 32;
		}
		if ('A' <= str[i] && str[i] <= 'Z')
		{
			if (('A' <= str[i - 1] && str[i - 1] <= 'Z')
				|| ('a' <= str[i - 1] && str[i - 1] <= 'z')
				|| ('0' <= str[i - 1] && str[i - 1] <= '9'))
				str[i] = str[i] + 32;
		}
		i++;
	}
	return (str);
}
