char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (*str)
	{
		if ('a' <= *str && *str <= 'z')
		{
			*str = *str - 32;
		}
		str++;
		i++;
	}
	str = str - i;
	return (str);
}
